package JavaOne.Operators.RelationalOperators;

public class RelationalOperators {
    public static void main(String[] args) {
        
        //output of relational operator is boolean

        System.out.println("10 > 2 :"+(10>2));
        System.out.println("10 < 2 :"+(10<2));
        System.out.println("10 >= 2 :"+(10>=2));
        System.out.println("10 <= 2 :"+(10<=2));
        System.out.println("10 == 2 :"+(10==2));
        System.out.println("10 != 2 :"+(10!=2));

    }
}
